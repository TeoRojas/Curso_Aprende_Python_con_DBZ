lista_dbz = ['Dragon', 'Ball', 'Z']

#Lista pequeña con las posiciones desde 0(incluida) hasta 1(excluida)
print(lista_dbz[:1])
#Valores que hay desde la posición 0(incluida) hasta la 1(excluida)
print(*lista_dbz[:1])   
#Valor que hay en la posición 1
print(lista_dbz[1])
#Lista pequeña con las posiciones desde 1(incluida) hasta la última
print(lista_dbz[1:])
#Valores que hay desde la posición 1 (incluida) hasta la última
print(*lista_dbz[1:])
#Lista con todos los valores
print(lista_dbz[:])
#Todos los valores de la lista
print(*lista_dbz[:])